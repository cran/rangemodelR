#' The package is usefull for generating randomized community matrices under
#' strict range cohesion. The package can handle data where species occurence
#' are recorded across one or two dimentions.One dimentional data are typically
#' for sites ordered along a variable of interest such
#' as elevation or latitude.\code{\link{rangemod.1d}} is used for such data. Two
#' dimentional data have species occurences recorded on grids with known
#' geographic coordinates and are handled with function \code{\link{rangemod.2d}}.
#' Range cohesion is enforced using an 'nb' object
#' (generated by \code{\link[spdep]{poly2nb}}
#' of the \pkg{\link[spdep]{spdep}} package).
